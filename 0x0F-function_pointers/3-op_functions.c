 #include "3-calc.h"
/**
 *op_add - function
 *@a: num
 *@b: num
 *Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - function
 *@a: num
 *@b: num
 *Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - function
 *@a: num
 *@b: num
 *Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - function
 *@a: num
 *@b: num
 *Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - function
 *@a: num
 *@b: num
 *Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
